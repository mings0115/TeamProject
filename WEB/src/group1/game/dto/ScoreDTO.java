package group1.game.dto;

public class ScoreDTO{
	
	private int scoreno;
	private int kill;
	private int death;
	private int assist;
	
	public int getScoreno() {
		return scoreno;
	}
	public void setScoreno(int scoreno) {
		this.scoreno = scoreno;
	}
	public int getKill() {
		return kill;
	}
	public void setKill(int kill) {
		this.kill = kill;
	}
	public int getDeath() {
		return death;
	}
	public void setDeath(int death) {
		this.death = death;
	}
	public int getAssist() {
		return assist;
	}
	public void setAssist(int assist) {
		this.assist = assist;
	}
	
	

}
